#include<stdio.h>
#include<math.h>
int main()
{
    double x = 4.6;
    double y = 8.0;

    //square root
    printf("square root of %.2f: %.2f\n",x,sqrt(x));

    //exponential function
    printf("exponential function, e^%.2f: %.2f\n",x,exp(x));

    //natural logrithm
    printf("natural logrithm of %.2f: %.2f\n",x,log(x));


    printf("commom logrithm(base 10) of %.2f: %.2f\n",x,log10(x));
    
    printf("ceil of %.2f: %.2f\n",x,ceil(x));

    printf("floor of %.2f: %.2f\n",x,floor(x));

    printf("round of %.2f: %.2f\n",x,round(x));

    printf("sine of %.2f radians: %.2f\n",x,sin(x));

    printf("cosine of %.2f radians: %.2f\n",x,cos(x));

    printf("tangent of %.2f radians: %.2f\n",x,tan(x));

    printf("arcsine of %.2f radians: %.2f\n",x,asin(x));

    printf("arccosine of %.2f radians: %.2f\n",x,acos(x));

    printf("arctangent of %.2f radians: %.2f\n",x,atan(x));

    printf(" %.2f raised to the power of %.2f: %.2f\n", x,y,pow(x,y));

    printf("absolute value of %.2f:%.2f\n",-x,fabs(-x));

    printf("hyperbolic sine of %.2f:%.2f\n",sinh(x));

    printf("hyperbolic cosine of %.2f:%.2f\n",cosh(x));

    printf("hyperbolic tangent of %.2f:%.2f\n",tanh(x));

    return 0;

}









    





        



    
    
    
     
    


    




